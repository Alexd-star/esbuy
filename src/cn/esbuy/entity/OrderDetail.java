package cn.esbuy.entity;

public class OrderDetail {
	private int odid;
	private int oid;
	private int gdid;
	private int odnum;
	public int getOdid() {
		return odid;
	}
	public void setOdid(int odid) {
		this.odid = odid;
	}
	public int getOid() {
		return oid;
	}
	public void setOid(int oid) {
		this.oid = oid;
	}
	public int getGdid() {
		return gdid;
	}
	public void setGdid(int gdid) {
		this.gdid = gdid;
	}
	public int getOdnum() {
		return odnum;
	}
	public void setOdnum(int odnum) {
		this.odnum = odnum;
	}
}
